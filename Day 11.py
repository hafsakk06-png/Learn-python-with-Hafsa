#Functions in Python (Part 1)
#What is a Function?

# A function is a block of code that runs only when it is called.
# It helps you avoid repeating code and makes your program organized.

#Function with Parameters

# You can pass data into a function using parameters.

def greet(name):
    print("Hello", name, "! How are you?")

greet("Hafsa")
greet("Muskan")

#Function with Return Value

# Functions can return data using the return keyword.

def add(a, b):
    return a + b

sum = add(5, 7)
print("Sum:", sum)

# Default Parameters

# You can set a default value for parameters.

def greet(name="Student"):
    print("Hello", name)

greet()
greet("Hafsa")

