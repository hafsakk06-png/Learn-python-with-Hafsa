#lets start with arithmetic operators

a = 10
b = 3

print("Addition:", a + b)      # 13
print("Subtraction:", a - b)   # 7
print("Multiplication:", a * b) # 30
print("Division:", a / b)       # 3.333...
print("Floor Division:", a // b) # 3
print("Modulus:", a % b)        # 1 (remainder)
print("Exponent:", a ** b)      # 1000 (10^3)

#now lets move to comparison operators
x = 5
y = 10
print("Equal:", x == y)          # False
print("Not Equal:", x != y)      # True
print("Greater than:", x > y)    # False
print("Less than:", x < y)       # True
print("Greater than or equal:", x >= y) # False
print("Less than or equal:", x <= y)    # True

#now lets move to logical operators
p = True
q = False
print("AND:", p and q)  # False
print("OR:", p or q)    # True
print("NOT p:", not p)   # False
print("NOT q:", not q)   # True

#now lets move to assignment operators
num = 10
print("Initial value:", num)  # 10
num += 5
print("After += 5:", num)      # 15
num -= 3
print("After -= 3:", num)      # 12
num *= 2
print("After *= 2:", num)      # 24
num /= 4
print("After /= 4:", num)      # 6.0
num //= 2
print("After //= 2:", num)     # 3.0
num %= 2
print("After %= 2:", num)      # 1.0
num **= 3
print("After **= 3:", num)     # 1.0

#now lets move to bitwise operators
a = 5  # (binary 0101)
b = 3  # (binary 0011)
print("AND:", a & b)  # 1 (binary 0001)
print("OR:", a | b)   # 7 (binary 0111)
print("XOR:", a ^ b)  # 6 (binary 0110)
print("NOT a:", ~a)   # -6 (binary ...1010)
print("a left shift 1:", a << 1) # 10 (binary 1010)
print("a right shift 1:", a >> 1) # 2 (binary 0010)
