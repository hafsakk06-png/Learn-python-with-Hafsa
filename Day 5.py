# Today we will learn about data types in Python
#          1. Numbers
# 1st There are 3 types of in python
# int --> whole numbers(e.g., 5, -12)
# float --> decimal numbers(e.g., 3.14, -0.99)
# complex --> numbers with real & imaginary parts (e.g., 2 + 3j)
x = 10        # int
y = 3.14      # float
z = 2 + 3j    # complex

print(type(x))
print(type(y))
print(type(z))

#     2. Strings
# Strings are sequences of characters enclosed in single or double quotes.
name = "Hafsa"
greeting = 'Hello Python!'

print(name)
print(greeting.upper())   # UPPERCASE
print(greeting.lower())   # lowercase
print(greeting[0])        # First character
print(greeting[0:5])      # Slicing

#     3. Booleans
# Booleans represent one of two values: True or False.
is_student = True
is_teacher = False

print(is_student)
print(5 > 3)   # True
print(2 == 5)  # False

#     4. Lists
# Lists are ordered collections of items (e.g., numbers, strings).
fruits = ["apple", "banana", "cherry"]
print(fruits)
print(fruits[1])      # Access 2nd element
fruits.append("mango") # Add element
print(fruits)

#     5. Tuples
# Tuples are similar to lists but are immutable (cannot be changed).
colors = ("red", "green", "blue")
print(colors[0])

#     6. Dictionaries
# Dictionaries store key-value pairs.

student = {
    "name": "Hafsa",
    "age": 20,
    "course": "Python"
}
print(student["name"])
print(student.get("course"))


