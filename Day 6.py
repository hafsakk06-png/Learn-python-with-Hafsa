#            STRING INDEXING
# In python, strings are indexed. The first character of the string has index 0, the second character has index 1, and so on.
# You can access individual characters of a string using their index.

word = "Python"
print(word[0])   # P
print(word[1])   # y
print(word[-1])  # n (last character)

#            STRINGS SLICING

text = "Hello World"
print(text[0:5])   # Hello
print(text[6:])    # World
print(text[:5])    # Hello

#         STRING METHODS

msg = " i love python "

print(msg.upper())      # I LOVE PYTHON
print(msg.lower())      # i love python
print(msg.strip())      # removes extra spaces
print(msg.replace("python", "C++"))  # i love C++
print(msg.split())      # ['i', 'love', 'python']

#         STRING + NUMBERS(CONCATENATION)

name = "Hafsa"
age = 19
print(name + " is " + str(age) + " years old.")

